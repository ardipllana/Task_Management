    private void addActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addActionPerformed
            try{
            UserProject b = new UserProject();
            
            b.setId(Integer.parseInt(id.getText()));
            b.setUsersId(ucbm.getElementAt(usercombo.getSelectedIndex()));
            b.setProjectId(pcbm.getElementAt(projectcombo.getSelectedIndex()));

            upir.create(b);
            idx++;
            clearFields();
            tableLoad();
             JOptionPane.showMessageDialog(this, "Data has been added successfully!");
       }
        catch(UserProjectException pe){
            JOptionPane.showMessageDialog(this, pe.getMessage());
        }
    }//GEN-LAST:event_addActionPerformed

