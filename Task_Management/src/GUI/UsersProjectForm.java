    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        int row = jTable1.getSelectedRow();
        UserProject b = this.uptm.getUserProject(row);
        b.setId(Integer.parseInt(id.getText()));
        b.setUsersId(ucbm.getElementAt(usercombo.getSelectedIndex()));
        b.setProjectId(pcbm.getElementAt(projectcombo.getSelectedIndex()));
        
        try {
            upir.edit(b);
        } catch (UserProjectException ex) {
            Logger.getLogger(UsersForm.class.getName()).log(Level.SEVERE, null, ex);
        }
        clearFields();
        tableLoad();
        JOptionPane.showMessageDialog(this, "Data has been updated successfully!");
    }//GEN-LAST:event_btnUpdateActionPerformed

    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        int row = jTable1.getSelectedRow();
           
        Object [] ob = {"YES","NO"};
        int i = javax.swing.JOptionPane.showOptionDialog(this, "Are you sure you want to delete this?", "DELETE", JOptionPane.OK_OPTION,JOptionPane.QUESTION_MESSAGE, null, ob, ob[1]);
                
        UserProject s = this.uptm.getUserProject(row);
        try {
            upir.remove(s);
            idx--;
        } catch (UserProjectException ex) {
            Logger.getLogger(UsersForm.class.getName()).log(Level.SEVERE, null, ex);
        }
        tableLoad();
        clearFields();
       JOptionPane.showMessageDialog(this, "Data has been deleted successfully!!");
    }//GEN-LAST:event_btnDeleteActionPerformed

