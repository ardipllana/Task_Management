    public void tableLoad(){
        List<UserProject> list=upir.findAll();
        uptm=new UserProjectTableModel(list);
        jTable1.setModel(uptm);
        uptm.fireTableDataChanged();
        tabelaSelectedIndexChange();
        id.setText(idx+"");
    }
    
    
    private void tabelaSelectedIndexChange(){
        final ListSelectionModel rowSM = jTable1.getSelectionModel();
        rowSM.addListSelectionListener(new ListSelectionListener(){
            
            @Override
            public void valueChanged(ListSelectionEvent Ise){
                if(Ise.getValueIsAdjusting())
                    return;
                ListSelectionModel rowSM = (ListSelectionModel)Ise.getSource();
                int selectedIndex = rowSM.getAnchorSelectionIndex();
                if (selectedIndex > -1){
                    UserProject users = uptm.getUserProject(selectedIndex);
                    
                    id.setText(users.getId()+"");
                    usercombo.setSelectedItem(users.getUsersId()+"");
                    projectcombo.setSelectedItem(users.getProjectId()+"");
                }
            }
        });
    }
    
    
    public void clearFields(){
           
         id.setText(idx+"");
         usercombo.setSelectedIndex(-1);
         projectcombo.setSelectedIndex(-1);
    }
    
    public void comboLoad(){
         
        List<Users> li= uir.findAll();
        ucbm = new UsersComboBoxModel(li);
        usercombo.setModel(ucbm);
         
        List<Project> lii= pir.findAll();
        pcbm = new ProjectComboBoxModel(lii);
        projectcombo.setModel(pcbm);
         
     }
    
    

